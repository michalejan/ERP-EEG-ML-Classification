"""## 4. Confusion-Matrix

    Extracts, sums, and normalizes confusion matrices across all subjects.
    Plots a 2x2 grid for the 4 intra-subject models.
    
"""

#aggregate and plot individual Normalized Confusion Matrices
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import os

def plot_individual_confusion_matrices(viz_dict, save_dir):
    # dicts to hold the cm
    cm_aggregate = {}
    subjects_per_model = {}

    # iterating through the dict
    for sub, sub_data in viz_dict.items():
        models_dict = sub_data.get('models', {})
        for model_name, metrics in models_dict.items():
            cm = metrics.get('confusion_matrix')

            if cm is not None:
                cm = np.array(cm)
                if model_name not in cm_aggregate:
                    cm_aggregate[model_name] = np.zeros_like(cm, dtype=float)
                    subjects_per_model[model_name] = 0
                cm_aggregate[model_name] += cm
                subjects_per_model[model_name] += 1

    # CLASS LABELS
    class_labels = ['Invalid Code', 'Valid Code']

    # generate each cm seperately
    for model_name, raw_cm in cm_aggregate.items():

        # SETUP OF THE FIGURE
        fig, ax = plt.subplots(figsize=(6, 5))

        # plotting the heatmap (raw counts, specific scale, unbolded text)
        sns.heatmap(raw_cm, annot=True, fmt='.0f', cmap='Blues', cbar=True,
                    xticklabels=class_labels, yticklabels=class_labels,
                    ax=ax, annot_kws={'size': 14},
                    vmin=800, vmax=1100,
                    cbar_kws={'ticks': [800, 850, 900, 950, 1000, 1050, 1100]})

        # formatting (only model name and n, no main suptitle)
        ax.set_title(f"{model_name} (n={subjects_per_model[model_name]})", fontsize=14, pad=10)
        ax.set_ylabel('True Label')
        ax.set_xlabel('Predicted Label')

        # adding border for academic styling
        for _, spine in ax.spines.items():
            spine.set_visible(True)
            spine.set_color('black')
            spine.set_linewidth(1)

        plt.tight_layout()

        # saving the figure
        fig_path = os.path.join(save_dir, f"CM_Intra_{model_name}.png")
        fig.savefig(fig_path, dpi=300, bbox_inches='tight')
        plt.show()

        print(f"Saved: {fig_path}")

# EXECUTING FUNCTION
plot_individual_confusion_matrices(viz_data, BASE_DIR)