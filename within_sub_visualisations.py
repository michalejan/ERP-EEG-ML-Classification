"""## 1. Within-Sub Metric Summary Bar Chart

"""

import matplotlib.pyplot as plt
import numpy as np
import seaborn as sns
import matplotlib.lines as mlines
import os

def plot_intra_subject_metrics(df, save_dir):
    # aggregate metrics across the sub
    agg_df = df.groupby('Model').agg(
        Mean_Accuracy=('Accuracy', lambda x: x.mean() * 100),
        Mean_STD=('STD', lambda x: x.mean() * 100),
        Pct_Significant=('p_value', lambda x: (x < 0.05).mean() * 100)
    ).reset_index() # Calculating percentage of subjects where p < 0.05 !!!!

    # sorting by accuracy
    agg_df = agg_df.sort_values(by='Mean_Accuracy', ascending=False)

    fig, ax = plt.subplots(figsize=(10, 6))

    x_pos = np.arange(len(agg_df['Model']))
    bar_width = 0.6

    # PLOTTING BARS
    bars = ax.bar(x_pos, agg_df['Mean_Accuracy'], bar_width,
                  yerr=agg_df['Mean_STD'], capsize=5,
                  color=sns.color_palette("Blues_r", len(agg_df)),
                  edgecolor='black', alpha=0.8)

    # formatting and threshold
    ax.set_ylabel('Mean Classification Accuracy (%)')
    ax.set_xlabel('Machine Learning Model')
    ax.set_title('5-fold Within-Subject Classification Performance (P600)', fontweight='bold', pad=15)
    ax.set_xticks(x_pos)
    ax.set_xticklabels(agg_df['Model'])

    # strcit 0-100 bound on y axis
    ax.set_ylim(0, 100)

    # chance level
    ax.axhline(y=50, color='red', linestyle='--', linewidth=1.5, label='Theoretical Chance (50%)')

    # annotations (Accuracy value and % Significant)
    for i, bar in enumerate(bars):
        acc_val = agg_df['Mean_Accuracy'].iloc[i]
        sig_val = agg_df['Pct_Significant'].iloc[i]

        y_text = acc_val + agg_df['Mean_STD'].iloc[i] + 2

        annotation_text = f"{acc_val:.1f}%\n({sig_val:.1f}% sig.)"
        ax.text(bar.get_x() + bar.get_width()/2, y_text,
                annotation_text, ha='center', va='bottom',
                fontsize=10, fontweight='bold', color='black')

    # LEGEND
    handles, labels = ax.get_legend_handles_labels()

    # PATCHED MARKER: Removed markeredgewidth, set moderate markersize=16, enforced black color
    sig_marker = mlines.Line2D([], [], color='none', marker=r'$\mathrm{sig.}$',
                               markersize=16, markerfacecolor='black', markeredgecolor='black', label='p<0.05')
    handles.append(sig_marker)
    labels.append('Subjects with p<0.05')

    ax.legend(handles=handles, labels=labels, loc='upper right')
    ax.grid(axis='y', linestyle='--', alpha=0.7)

    plt.tight_layout()

    # EXPORT LOGIC
    fig_path = os.path.join(save_dir, 'within-sub_class_results.png')
    fig.savefig(fig_path, dpi=300, bbox_inches='tight')
    plt.show()

    print(f"Saved: {fig_path}")

# EXECUTING THE FUNCTION
