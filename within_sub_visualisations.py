"""## 2. Within-Subject Expertise Comparison

    Generates a grouped bar chart comparing Novice vs.
    Expert decoding accuracies. Includes scaled error bars and subgroup-specific significance percentages.




"""

import matplotlib.lines as mlines
import matplotlib.pyplot as plt
import numpy as np
import os

def plot_expertise_comparison(df, save_dir):
    # aggregate metrics by MODEL and EXPERTISE
    agg_df = df.groupby(['Model', 'Expertise']).agg(
        Mean_Accuracy=('Accuracy', lambda x: x.mean() * 100),
        Mean_STD=('STD', lambda x: x.mean() * 100),
        Pct_Significant=('p_value', lambda x: (x < 0.05).mean() * 100)
    ).reset_index()

    # model ordering
    model_order = df.groupby('Model')['Accuracy'].mean().sort_values(ascending=False).index

    # setup figure
    fig, ax = plt.subplots(figsize=(12, 7))

    x = np.arange(len(model_order))
    width = 0.35

    # COLOR-PALETTE
    # colors = {'Novice': '#74a9cf', 'Expert': '#045a8d'}
    colors = {'Novice': '#1f78b4', 'Expert': '#d62728'}

    # PLOTTING BARS
    for i, expertise in enumerate(['Novice', 'Expert']):
        exp_data = agg_df[agg_df['Expertise'] == expertise]
        exp_data = exp_data.set_index('Model').reindex(model_order).reset_index()

        # calculating offset
        offset = (i - 0.5) * width
        x_pos = x + offset

        bars = ax.bar(x_pos, exp_data['Mean_Accuracy'], width,
                      yerr=exp_data['Mean_STD'], capsize=5,
                      label=expertise, color=colors.get(expertise, 'gray'),
                      edgecolor='black', alpha=0.9)

        # annotations (Accuracy and % Significant per subgroup)
        for j, bar in enumerate(bars):
            acc_val = exp_data['Mean_Accuracy'].iloc[j]
            sig_val = exp_data['Pct_Significant'].iloc[j]
            std_val = exp_data['Mean_STD'].iloc[j]

            if np.isnan(acc_val):
                continue

            y_text = acc_val + std_val + 2
            annotation_text = f"{acc_val:.1f}%\n({sig_val:.0f}% sig.)"

            ax.text(bar.get_x() + bar.get_width()/2, y_text,
                    annotation_text, ha='center', va='bottom',
                    fontsize=9, fontweight='bold', color='black')

    # formatting and threshold
    ax.set_ylabel('Mean Classification Accuracy (%)')
    ax.set_xlabel('Machine Learning Model')
    ax.set_title('5-fold Within-Subject Classification: Novices vs. Experts (P600)', fontweight='bold', pad=15)
    ax.set_xticks(x)
    ax.set_xticklabels(model_order)
    ax.set_ylim(0, 100)

    ax.axhline(y=50, color='red', linestyle='--', linewidth=1.5, label='Theoretical Chance (50%)')

    # LEGEND
    handles, labels = ax.get_legend_handles_labels()
    sig_marker = mlines.Line2D([], [], color='none', marker=r'$\mathrm{sig.}$',
                               markersize=22, markerfacecolor='black', label='p<0.05')
    handles.append(sig_marker)
    labels.append('Subjects with p<0.05')

    ax.legend(handles=handles, labels=labels, title='Expertise Level', loc='upper right')
    ax.grid(axis='y', linestyle='--', alpha=0.7)

    plt.tight_layout()

    # EXPORT
    fig_path = os.path.join(save_dir, 'classifier_expertise.png')
    fig.savefig(fig_path, dpi=300, bbox_inches='tight')
    plt.show()

    print(f"Saved: {fig_path}")

# EXECUTING FUNCTION
plot_expertise_comparison(df_intra_results, FIG_DIR)

"""## 3. Within-Subject Edge Case Analysis (TABLE)

    Identifies the top 3 and bottom 3 subjects by mean accuracy,
    and formats their metrics into a publication-ready pivoted table.

"""

import os
import pandas as pd
from IPython.display import display

# dir
FIG_DIR = os.path.join(BASE_DIR, 'figures')
if not os.path.exists(FIG_DIR):
    os.makedirs(FIG_DIR)
    print(f"Created directory: {FIG_DIR}")

def generate_extremes_table(df):
    # ranking subs by acc
    sub_perf = df.groupby(['Subject', 'Expertise'])['Accuracy'].mean().sort_values(ascending=False).reset_index()

    top_3 = sub_perf.head(3)['Subject'].tolist()
    bot_3 = sub_perf.tail(3)['Subject'].tolist()

    # filtering for 3 bets and 3 worst subs
    df_filtered = df[df['Subject'].isin(top_3 + bot_3)].copy()

    # assigning performance
    df_filtered['Performance_Tier'] = df_filtered['Subject'].apply(
        lambda x: 'Top 3 (Highest Decodability)' if x in top_3 else 'Bottom 3 (Lowest Decodability)'
    )

    # formatting metrics
    def format_metrics(row):
        acc = row['Accuracy'] * 100
        std = row['STD'] * 100
        p = row['p_value']
        # Format p-value strictly
        p_str = "<0.001" if p < 0.001 else f"={p:.3f}"
        return f"{acc:.1f}% ± {std:.1f} (p{p_str})"

    df_filtered['Formatted_Metrics'] = df_filtered.apply(format_metrics, axis=1)

    # TABLE OUTPUT
    clean_table = df_filtered.pivot(
        index=['Performance_Tier', 'Subject', 'Expertise'],
        columns='Model',
        values='Formatted_Metrics'
    )

    # ascending order
    clean_table = clean_table.reindex(['Top 3 (Highest Decodability)', 'Bottom 3 (Lowest Decodability)'], level=0)

    return clean_table

pd.set_option('display.max_columns', None)
